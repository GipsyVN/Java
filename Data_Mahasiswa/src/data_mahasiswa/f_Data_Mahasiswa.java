    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int i = tabel.getSelectedRow();
        if(i == -1){
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Baris Data Terlebih Dahulu");
            return;
        }
        
        int opsi = JOptionPane.showConfirmDialog(null, "Yakin Menghapus Data?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        if(opsi == JOptionPane.YES_OPTION){
            String nim = (String) tb.getValueAt(i, 1);
            try{
                Koneksi.koneksi();
                String sql = "delete from mahasiswa where NIM = '" + nim + "'";
                Statement stm = con.createStatement();
                stm.executeUpdate(sql);
                
                loadData();
                JOptionPane.showMessageDialog(null, "Berhasil Menghapus Data");
                btnClearActionPerformed(null);
                stm.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Gagal Menghapus Data\n" + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        DefaultTableModel dtm = (DefaultTableModel) tabel.getModel(); 
        int selectedRowIndex = tabel.getSelectedRow();
        String nim = (String) dtm.getValueAt(selectedRowIndex, 1);
        try {
            Koneksi.koneksi();
            String sql= "Select * from mahasiswa where NIM = '"+ nim +"'";
            Statement pst = con.prepareStatement(sql); 
            ResultSet rs = pst.executeQuery(sql);
            if(rs.next()){
                no = rs.getString("No");
                RowSelect.set_no(no);
                txtNIM.setText(rs.getString("NIM"));
                txtNama.setText(rs.getString("Nama"));
                txtTempat.setText(rs.getString("Tempat_Lahir"));
                dtTanggal.setDate(rs.getDate("Tanggal_Lahir"));
                txtProdi.setText(rs.getString("Prodi"));
                txtAsal.setText(rs.getString("Asal_Sekolah"));
                txtAlamat.setText(rs.getString("Alamat"));
            }
            con.close();
            pst.close();
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Terjadi Error\n" + ex.getMessage());
        }
    }//GEN-LAST:event_tabelMouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String no = null;
        String nim = txtNIM.getText();
        String nama = txtNama.getText();
        String tempat = txtTempat.getText();
        Date tanggal = dtTanggal.getDate();
        String prodi = txtProdi.getText();
        String asal = txtAsal.getText();
        String alamat = txtAlamat.getText();
        
        if(nim.isEmpty() || nama.isEmpty() || tempat.isEmpty() || tanggal == null || prodi.isEmpty() || asal.isEmpty() || alamat.isEmpty()){
            JOptionPane.showMessageDialog(null, "Data Tidak Lengkap\nSilahkan Isi Data Secara Lengkap");
        }else{
            int opsi = JOptionPane.showConfirmDialog(null, "Yakin Menyimpan Data?", "Simpan Data", JOptionPane.YES_NO_OPTION);
            if(opsi == JOptionPane.YES_OPTION){
                try{
                    Koneksi.koneksi();
                    String sql = "insert into mahasiswa values (?, ?, ?, ?, ?, ?, ?, ?)";
                    PreparedStatement pst = con.prepareStatement(sql);
                    
                    pst.setString(1, no);
                    pst.setString(2, nim);
                    pst.setString(3, nama);
                    pst.setString(4, tempat);
                    pst.setDate(5, new java.sql.Date(tanggal.getTime()));
                    pst.setString(6, prodi);
                    pst.setString(7, asal);
                    pst.setString(8, alamat);
                    
                    pst.executeUpdate();
                    
                    loadData();
                    JOptionPane.showMessageDialog(null, "Berhasil Menyimpan Data");
                    pst.close();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Gagal Menyimpan Data\n" + e.getMessage());
                    //System.out.println(e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

