    public void loadData(){
        tb.getDataVector().removeAllElements();
        tb.fireTableDataChanged();
        
        try{
            Koneksi.koneksi();
            Statement stm = con.createStatement();
            String sql = "select * from mahasiswa";
            ResultSet rs = stm.executeQuery(sql);
            while(rs.next()){
                Object[] o = new Object[8];
                o[1] = rs.getString("NIM");
                o[2] = rs.getString("Nama");
                o[3] = rs.getString("Tempat_Lahir");
                o[4] = rs.getDate("Tanggal_Lahir");
                o[5] = rs.getString("Prodi");
                o[6] = rs.getString("Asal_Sekolah");
                o[7] = rs.getString("Alamat");
                tb.addRow(o);
            }
            noUrut();
            stm.close();
            rs.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Gagal Load Data\n" + e.getMessage());
            //System.out.println(e.getMessage());
        }
    }
    
    public void noUrut(){
        int baris = tb.getRowCount();
        for(int i = 0; i < baris; i++){
            String no = String.valueOf(i + 1);
            tb.setValueAt(no, i, 0);
        }
    }
    
    public void isi(){
        String nim = txtNIM.getText();
        try {
            Koneksi.koneksi();
            String sql= "Select * from mahasiswa where NIM = '"+ nim +"'";
            Statement pst = con.prepareStatement(sql); 
            ResultSet rs = pst.executeQuery(sql);
            if(rs.next()){
                txtNama.setText(rs.getString("Nama"));
                txtTempat.setText(rs.getString("Tempat_Lahir"));
                dtTanggal.setDate(rs.getDate("Tanggal_Lahir"));
                txtProdi.setText(rs.getString("Prodi"));
                txtAsal.setText(rs.getString("Asal_Sekolah"));
                txtAlamat.setText(rs.getString("Alamat"));
            }
            con.close();
            pst.close();
        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Gagal Load Data" + ex.getMessage());
            //System.out.println(ex.getMessage());
        }
    }
